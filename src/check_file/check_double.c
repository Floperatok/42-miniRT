#include "minirt.h"

int	check_double(char *f)
{
	int	dots;
	int	i;

	dots = 0;
	i = 0;
	if (f[0] == '-' || f[0] == '+')
		i++;
	if (f[i] == '.' || f[ft_strlen(f) - 1] == '.')
		return (0);
	while (f[i])
	{
		if ((f[i] < '0' || f[i] > '9') && f[i] != '.')
			return (0);
		if (dots > 1)
			return (0);
		if (f[i] == '.')
			dots++;
		i++;
	}
	return (1);
}
